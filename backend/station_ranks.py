def get_station_dwell_time(station_id: str) -> int:
    """
    駅IDから停車時間を取得する。
    定義がない場合はデフォルト20秒を返す。
    """
    # stop_id は文字列として扱う
    str_id = str(station_id)
    return STATION_RANKS.get(str_id, 20)